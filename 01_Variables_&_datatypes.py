# Variables are like containers used to store information i.e.data.
num = 12        # integer data type
f = 567.29      # float data type
word = "Code"   # string data type
print(type(num))
print(type(f))
print(type(word))

# Type casting :
# One data type can be converted into another by type casting

#Example 1.
print("\nExample 1. string to integer")
a = "9856"   # data type of a is string here
print(type(a))
a = int(a)   # data type of a became integer here
print(a, type(a))

a = float(a)   # data type of a became float here
print(a, type(a))

